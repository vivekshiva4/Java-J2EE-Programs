
public class Calculator {
	int x = 10;
	int y = 2;
	

	public void add() {
		int z = x + y;
		System.out.println("addition of x and y is  :" + z);
	}

	public void sub() {
		int z = x - y;
		System.out.println("substraction of x and y is  :" + z);
	}

	public void mul() {
		int z = x * y;
		System.out.println("multiplication of x and y is  :" + z);
	}

	public void div() {
		double z = x / y;
		System.out.println("division of x and y is  :" + z);
	}

	
}
